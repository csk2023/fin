#Numeric Data Type
x<-10
y<-3.14
print(c(x,y))

#Integer Data Type
a<-10L
b<-as.integer(20)
print(c(a,b))

#Logical Data Type
is_true<-TRUE
is_false<-FALSE
print(is_true)
print(is_false)

#Character Data Type
name<-"John"
message<-"Hello World"
print(name)
print(message)

#Complex Data Type 
c1<-complex(real=3,imaginary=2)
c2<-2+3i
print(c1)
print(c2)

#Raw Data Type 
raw_data<-as.raw(c(0x48,0x65,0x6C,0x6C,0x6F))
print(raw_data)


raw_data<-as.raw(c(2x48,0x65,0x6C,0x6C,0x6F))
print(raw_data)
